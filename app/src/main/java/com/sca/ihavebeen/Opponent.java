public class Opponent {

    public String mOppName;
    public int mOppGamesWon;
    public int mOppGamesLost;
    public String mOppMotto;

    public Opponent() {
    }

    public String getmOppName() {
        return mOppName;
    }

    public void setmOppName(String mOppName) {
        this.mOppName = mOppName;
    }

    public int getmOppGamesWon() {
        return mOppGamesWon;
    }

    public void setmOppGamesWon(int mOppGamesWon) {
        this.mOppGamesWon = mOppGamesWon;
    }

    public int getmOppGamesLost() {
        return mOppGamesLost;
    }

    public void setmOppGamesLost(int mOppGamesLost) {
        this.mOppGamesLost = mOppGamesLost;
    }

    public String getmOppMotto() {
        return mOppMotto;
    }

    public void setmOppMotto(String mOppMotto) {
        this.mOppMotto = mOppMotto;
    }
}
